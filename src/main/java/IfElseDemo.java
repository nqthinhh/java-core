public class IfElseDemo {
    public static void main(String[] args) {
        int month = 1;
        if (month == 31){
            System.out.println("Co 31 ngay");
        }else if(month == 30){
            System.out.println("Co 30 ngay");
        }else{
            System.out.println("Khong biet");
        }
    }
}
